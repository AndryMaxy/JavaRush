package com.javarush.task.task04.task0433;


/* 
Гадание на долларовый счет
*/

import java.io.*;

public class Solution {
    public static void main(String[] args) throws Exception {
        //напишите тут ваш код
        int i = 10;
        int k = 10;

        while (0 < i) {
            System.out.println("SSSSSSSSSS");
            while (0 < k) {
                System.out.print("S");
                k--;
            }
            i--;
        }
    }
}
